let x = 10;

// post decrement operator. x will be decremented later in the code (once called again)
console.log(x--); // 10
// pre decrement operator. x is already equal to 9 (from previous post-decrement) and is decremented in-place to 8
console.log(--x); // 8
// post increment operator. x will be incremented later in the code (once called again)
console.log(x++); // 8